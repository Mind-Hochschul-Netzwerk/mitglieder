Du nimmst zur Kenntnis, dass deine personenbezogenen Daten verarbeitet und 
gespeichert werden. Dies umfasst nach DSGVO Art. 6 Abs. 1 lit. a und f Daten, 
die zur Entscheidung über die Aufnahme notwendig sind. Die Daten werden 
ausschließlich dafür genutzt und nicht ohne deine Zustimmung weitergegeben. 
Diese Daten können während der Bearbeitung des Antrags nur von Personen 
eingesehen werden, die über deinen Aufnahmeantrag entscheiden. Die 
eingegebenen Daten werden auf dem MHN-Server solange gespeichert, bis über 
deinen Antrag abschließend entschieden worden ist. Bei einem positiven 
Bescheid werden die Daten in die MHN-Mitgliederdatenbank (ebenfalls auf dem 
MHN-Server) übertragen. Die Daten dienen dann dem Zwecke der Vernetzung der 
Mitglieder und Vereinsarbeit. Sie werden ausschließlich dafür genutzt und 
nicht ohne deine Zustimmung weitergegeben. Einsehen können diese Daten 
zunächst nur die Personen, die mit der MHN-Mitgliederbetreuung oder der 
MHN-Systemadministration betraut sind; du kannst sie freiwillig für alle 
Mitglieder sichtbar machen. Du hat das Recht, Auskunft über die über dich 
gespeicherten Daten zu erhalten, sie zu berichtigen und zu löschen, ihre 
Verarbeitung einzuschränken oder ihr zu widersprechen sowie Beschwerde bei 
einer Aufsichtsbehörde einzulegen. Zudem hast du jederzeit das Recht, dieser 
Einwilligung zu widersprechen. Bitte wende dich dazu an das zuständige 
Vorstandsmitglied unter vorstand@mind-hochschul-netzwerk.de . Weiterführende 
Informationen zur Datenverarbeitung bei MHN findest du in der 
<a href=https://www.mind-hochschul-netzwerk.de/index.php/datenschutz/>
Datenverarbeitungsrichtlinie</a>.
